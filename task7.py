s = "international"
print("1.Третий символ: " +s[2])
print("2.Последний символ: " +s[-1])
print("3.Первые 5 символов: " +s[0:5])
print("4.Вся строка, кроме последних двух символов: " +s[0:-2])
print("5.Символы с четными индексами: " +s[::2])
print("6.Символы с нечетными индексами: " +s[1::2])
print("7.Символы в обратном порядке: " +s[::-1])
print("8.Символы через один в обратном порядке: " +s[-1::-2])
print("9.Длина данной строки: " +str(len(s))+ " символов")